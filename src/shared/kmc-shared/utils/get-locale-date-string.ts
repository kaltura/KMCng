function getNavigatorLanguage () {
    return navigator.languages && navigator.languages.length
        ? navigator.languages[0]
        : (<any>navigator).userLanguage || navigator.language || (<any>navigator).browserLanguage || 'en-US';
}

export function getLocaleDateString(): string {
    const dateFormat = formats[getNavigatorLanguage()];
    return enableDateByLocale && dateFormat ? dateFormat : 'MM/DD/YY';
}

export function getCalendarFormat(): string {
    const dateFormat = calendarFormats[getNavigatorLanguage()];
    return enableDateByLocale && dateFormat ? dateFormat : 'mm/dd/yy';
}
